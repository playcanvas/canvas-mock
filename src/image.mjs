class Image {
    /** @type {number} */
    width;

    /** @type {number} */
    height;

    /**
     * @param {number} width - The width of the image.
     * @param {number} height - The height of the image.
     */
    constructor(width, height) {
        this.width = width;
        this.height = height;
    }
}

export { Image };
